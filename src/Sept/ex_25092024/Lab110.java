package Sept.ex_25092024;

public class Lab110 {
    public static void main(String[] args) {
        // while
        // while - A, B, C
        // A - Initialization
        // B - Condition
        // C - Increment / Decrement

        int i = 0; //A
        while (i <= 10) { //B
            System.out.println(i);
            i++; // C
        }
    }
}
