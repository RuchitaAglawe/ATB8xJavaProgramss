package Sept.TASK;

public class ex_27092024_Sum_of_number {
    public static void main(String[] args) {
        //sum of number from 1 to 100 using a while loop.
        int i=1,sum=0;
        while(i<=100)
        {
            sum=sum+i;
            i++;
        }
        System.out.println("sum of numbers from 1 to 100 => "+sum);

    }
}
