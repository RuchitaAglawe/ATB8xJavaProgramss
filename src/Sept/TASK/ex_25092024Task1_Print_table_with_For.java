package Sept.TASK;

public class ex_25092024Task1_Print_table_with_For {
    public static void main(String[] args) {
        int n=10;

        // For loop to generate the table of 10 with For
        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " x " + i + " = " + (n * i));
        }
    }
}

